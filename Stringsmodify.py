
#Upper case

a = "hello,world!"
print(a.upper())

# lower case

a = "HELLO,WORLD!"
print(a.lower())



#Remove whitespace by using strip()

a = "   Machine learning   "

print(a.strip())


#Replace string by replace()


a = "Machine learning"
print(a.replace("Machine","AI"))


#Split string

a = 'Machine_learning_Artificial_intelligence'
print(a.split('_'))


